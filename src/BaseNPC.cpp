#include "BaseNPC.h"
#include "Define.h"

/*------------------------------------------------------------------------------
コンストラクタ
------------------------------------------------------------------------------*/
BaseNPC::BaseNPC(InterfaceNPCEvent* aNPCEvent)
{
	// 変数
	mNPCEvent = aNPCEvent;

	mPtn = 0;
	mColor = 0;
	mStrikenAnime = 0;
	mIsStriken = false;

	// オブジェクト
	mPlayer = 0;
}

/*------------------------------------------------------------------------------
デストラクタ
------------------------------------------------------------------------------*/
BaseNPC::~BaseNPC()
{
}

/*------------------------------------------------------------------------------
カラーをセット
------------------------------------------------------------------------------*/
void BaseNPC::setColor(int aColor)
{
	mColor = aColor;
}

/*------------------------------------------------------------------------------
倒されたかフラグを返す
------------------------------------------------------------------------------*/
bool BaseNPC::getIsStriken()
{
	return mIsStriken;
}

/*------------------------------------------------------------------------------
エリアが変わる瞬間に実行される
------------------------------------------------------------------------------*/
void BaseNPC::areaChange()
{
	mY += Define::MapHeight;
}